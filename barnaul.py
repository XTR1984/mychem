def makesuperstar(ox,oy, n):
    D=20
    f = 0
    R = D*n/PI/4*2
    for i in range(0,n):
            x = ox+cos(f)*R
            y = oy-sin(f)*R
            a1= Atom(x, y, 3,2*PI/n*i)
            space.appendatom(a1)
            x = ox+cos(f)*(R+D)
            y = oy-sin(f)*(R+D)
            a1= Atom(x, y, 2, 2*PI/n*i)
            space.appendatom(a1)
            x = ox+cos(f)*(R+D*2)
            y = oy-sin(f)*(R+D*2)
            a1= Atom(x, y, 1,2*PI/n*i+PI)
            space.appendatom(a1)
            f+=2*PI/n




